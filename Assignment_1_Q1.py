# Write a Python program that does the following:
#1.  Takes two numbers as input from the user.
# 2.  Performs the basic mathematical operations on these two numbers: Addition, Subtraction, Multiplication, Division
# Displays the results of each operation on the screen.

a = float(input("Enter the first number: "))
b = float(input("Enter the second number: "))
print("The sum of two numbers is: ", a + b)
print("The difference of two numbers is: ", a - b)
print("The product of two numbers is: ", a * b)
print("The division of two numbers is: ", a / b)